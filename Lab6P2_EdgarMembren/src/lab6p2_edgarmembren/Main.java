    private void jb_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_loginActionPerformed

        try {
            String user = tf_login_user.getText();
            String pass = tf_login_pass.getText();
            adminCreativos ac = new adminCreativos("./creativos.txt");
            ac.cargarArchivo();
            adminEstudiantes ae = new adminEstudiantes("./estudiantes.txt");
            ae.cargarArchivo();
            adminEjecutivos aej = new adminEjecutivos("./ejecutivos.txt");
            aej.cargarArchivo();
            adminNormales an = new adminNormales("./normales.txt");
            an.cargarArchivo();

            for (int i = 0; i < ac.getListaCreativos().size(); i++) {
                if (ac.getListaCreativos().get(i).getUser().equals(user) && ac.getListaCreativos().get(i).getPass().equals(pass)) {
                    jd_NotasCreativos.setModal(true);
                    jd_NotasCreativos.pack();
                    jd_NotasCreativos.setLocationRelativeTo(this);
                    jd_NotasCreativos.setVisible(true);

                }
            }
            for (int i = 0; i < ae.getListaEstudiantes().size(); i++) {
                if (ae.getListaEstudiantes().get(i).getUser().equals(user) && ae.getListaEstudiantes().get(i).getPass().equals(pass)) {
                    jd_NotasEstu.setModal(true);
                    jd_NotasEstu.pack();
                    jd_NotasEstu.setLocationRelativeTo(this);
                    jd_NotasEstu.setVisible(true);

                }

            }
            for (int i = 0; i < aej.getListaEjecutivos().size(); i++) {
                if (aej.getListaEjecutivos().get(i).getUser().equals(user) && aej.getListaEjecutivos().get(i).getPass().equals(pass)) {
                    jd_NotasEje.setModal(true);
                    jd_NotasEje.pack();
                    jd_NotasEje.setLocationRelativeTo(this);
                    jd_NotasEje.setVisible(true);

                }
            }
            for (int i = 0; i < an.getListaNormales().size(); i++) {
                if (an.getListaNormales().get(i).getUser().equals(user) && an.getListaNormales().get(i).getPass().equals(pass)) {
                    jd_NotasNormales.setModal(true);
                    jd_NotasNormales.pack();
                    jd_NotasNormales.setLocationRelativeTo(this);
                    jd_NotasNormales.setVisible(true);

                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error");
        }


    }//GEN-LAST:event_jb_loginActionPerformed

